// @ts-check
/**
 * Deterministic core benchmark (Phase 4.5). Times the pure read-model derivations the AI
 * Platform (Phase 5) calls on every request — the intelligence dashboard composition, attention
 * engine, signals, review snapshot and resource portfolio — over synthetic inputs. These are
 * pure functions, so the numbers are stable and comparable release-to-release: this is the
 * performance BASELINE Phase 5 must not regress.
 *
 * The measurement itself lives in packages/core/src/intelligence/baseline.perf.test.ts (it runs
 * under vitest, which already resolves the `@myos/core/*` workspace subpath exports and strips
 * TS). This script spawns that vitest run, parses the `BENCH <name> mean=.. p95=..` lines it
 * prints, and — with `--write` — refreshes docs/performance/baseline.md.
 *
 * Run: node scripts/benchmark.mjs            (add --write to refresh the doc)
 */
import { writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";
import { spawnSync } from "node:child_process";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const coreDir = join(root, "packages/core");

// Run the perf test through vitest (resolves the workspace + strips TS for us).
const res = spawnSync(
  process.platform === "win32" ? "pnpm.cmd" : "pnpm",
  ["exec", "vitest", "run", "src/intelligence/baseline.perf.test.ts", "--pool=forks"],
  { cwd: coreDir, encoding: "utf8", shell: process.platform === "win32" },
);

const out = `${res.stdout ?? ""}\n${res.stderr ?? ""}`;
const results = [...out.matchAll(/BENCH\s+(\S+)\s+mean=([\d.]+)us\s+p95=([\d.]+)us/g)].map((m) => ({
  name: m[1],
  mean: Number(m[2]),
  p95: Number(m[3]),
}));

if (results.length === 0) {
  console.error("No BENCH lines captured — vitest run failed:\n", out);
  process.exit(1);
}

console.log("Core derivation benchmark (µs per call)\n=======================================");
for (const r of results) {
  console.log(
    `${r.name.padEnd(38)} mean ${r.mean.toFixed(1).padStart(8)}  p95 ${r.p95.toFixed(1).padStart(8)}`,
  );
}

if (process.argv.includes("--write")) {
  const lines = [
    "# Performance Baseline — core derivations (Phase 4.5)",
    "",
    "Generated by `scripts/benchmark.mjs` (which runs `packages/core/src/intelligence/baseline.perf.test.ts` under vitest). These are the pure read-model derivations the AI Platform (Phase 5) calls on every request. They are pure functions of their input, so the numbers below are the stable baseline Phase 5 must not regress.",
    "",
    "| Derivation | Mean (µs) | p95 (µs) |",
    "| --- | ---: | ---: |",
    ...results.map((r) => `| \`${r.name}\` | ${r.mean.toFixed(1)} | ${r.p95.toFixed(1)} |`),
    "",
    "Measured over 5,000 iterations each. Every derivation completes in well under a millisecond, so composition cost is negligible next to DB round-trips — the AI layer can call these freely.",
    "",
  ];
  writeFileSync(join(root, "docs/performance/baseline.md"), lines.join("\n") + "\n");
  console.log("\nWrote docs/performance/baseline.md");
}
