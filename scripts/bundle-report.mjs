// @ts-check
/**
 * Bundle-size report (Phase 4.5). Reads the Next.js production build manifest
 * (apps/web/.next/app-build-manifest.json) and, for every app route, sums the on-disk byte
 * size of the JS chunks it loads to produce a first-load-JS figure per route. This is the
 * bundle BASELINE — Phase 5's AI surfaces must not balloon these routes.
 *
 * Requires a production build to have run first (`pnpm --filter @myos/web build`). A pure
 * filesystem read otherwise — no server, no Postgres. `--write` refreshes
 * docs/performance/bundle.md.
 *
 * Run: node scripts/bundle-report.mjs   (add --write to refresh the doc)
 */
import { readFileSync, writeFileSync, existsSync, statSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const nextDir = join(root, "apps/web/.next");
const manifestPath = join(nextDir, "app-build-manifest.json");

if (!existsSync(manifestPath)) {
  console.error(
    `No build manifest at ${manifestPath}. Run \`pnpm --filter @myos/web build\` first.`,
  );
  process.exit(1);
}

/** @type {{ pages: Record<string, string[]> }} */
const manifest = JSON.parse(readFileSync(manifestPath, "utf8"));

/** Byte size of a static chunk on disk (0 if absent — e.g. a dev-only manifest). */
const chunkBytes = (chunk) => {
  const p = join(nextDir, chunk);
  return existsSync(p) ? statSync(p).size : 0;
};

// Shared chunks loaded by every page (the framework/runtime baseline).
const pageEntries = Object.entries(manifest.pages);
const shared = pageEntries.reduce((acc, [, chunks], i) => {
  const set = new Set(chunks);
  return i === 0 ? set : new Set([...acc].filter((c) => set.has(c)));
}, new Set());
const sharedBytes = [...shared].reduce((s, c) => s + chunkBytes(c), 0);

const rows = pageEntries
  .filter(([route]) => route.endsWith("/page"))
  .map(([route, chunks]) => {
    const total = chunks.reduce((s, c) => s + chunkBytes(c), 0);
    const routeOnly = chunks.filter((c) => !shared.has(c)).reduce((s, c) => s + chunkBytes(c), 0);
    return { route: route.replace(/\/page$/, "") || "/", firstLoad: total, routeOnly };
  })
  .sort((a, b) => b.firstLoad - a.firstLoad);

const kb = (n) => `${(n / 1024).toFixed(1)} kB`;

console.log("Bundle report (first-load JS per route)\n=======================================");
console.log(`Shared baseline: ${kb(sharedBytes)}`);
for (const r of rows)
  console.log(`${r.route.padEnd(28)} ${kb(r.firstLoad).padStart(10)}  (route ${kb(r.routeOnly)})`);

if (process.argv.includes("--write")) {
  const lines = [
    "# Bundle Report — first-load JS per route (Phase 4.5)",
    "",
    "Generated by `scripts/bundle-report.mjs` from the Next.js production build manifest. First-load JS = every chunk a route loads (shared framework baseline + route-specific code). This is the bundle baseline Phase 5's AI surfaces must not regress.",
    "",
    `- Shared baseline (loaded by every route): **${kb(sharedBytes)}**`,
    `- Routes measured: **${rows.length}**`,
    "",
    "| Route | First-load JS | Route-specific |",
    "| --- | ---: | ---: |",
    ...rows.map((r) => `| \`${r.route}\` | ${kb(r.firstLoad)} | ${kb(r.routeOnly)} |`),
    "",
  ];
  writeFileSync(join(root, "docs/performance/bundle.md"), lines.join("\n") + "\n");
  console.log("\nWrote docs/performance/bundle.md");
}
